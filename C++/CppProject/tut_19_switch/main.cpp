#include <iostream>

using namespace std;

int main(int argc, char** argv) {
    int age = 20;
    
    switch(age){
        
        case 10:
            cout << "Case 10" << endl;
            break;
            
        case 20:
            cout << "Case 20" << endl;
            break;
            
        case 30:
            cout << "Case 30" << endl;
            break;
            
        case 40:
            cout << "Case 40" << endl;
            break;
            
        default:
            cout << "default case" << endl;
                    
            
    }
    return 0;
}

